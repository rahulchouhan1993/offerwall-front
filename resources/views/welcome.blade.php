         $(document).ready(function () {
            function menuOnScroll(mySection, myMenu, myClass) {
               $(window).on("scroll", function () {
                     var elScroll = $(window).scrollTop();
                     var navHeight = $("nav").outerHeight();
                     var currentSection = null;

                     $(mySection).each(function () {
                        var sectionTop = $(this).offset().top - navHeight;
                        var sectionBottom = sectionTop + $(this).outerHeight();

                        if (elScroll >= sectionTop && elScroll < sectionBottom) {
                           currentSection = this;
                        }
                     });

                     if (currentSection) {
                        var id = $(currentSection).attr("id");
                        $(myMenu).removeClass(myClass);
                        $('nav ul li a[href="#' + id + '"]').addClass(myClass);
                     }
               });
            }

            function scrollToAnyPoint(navItem) {
               $(navItem).on("click", function (e) {
                     e.preventDefault();
                     var target = $(this).attr("href");

                     if ($(target).length) {
                        var toSection = $(target).offset().top - $("nav").outerHeight() + 1; // Small offset to avoid edge case

                        $("html, body").animate(
                           { scrollTop: toSection },
                           700,
                           function () {
                                 $(window).trigger("scroll"); // Ensure active class updates after scroll
                           }
                        );
                     }
               });
            }

            // Initialize functions
            menuOnScroll("section", "nav ul li a", "inSection");
            scrollToAnyPoint("nav ul li a");
         });


